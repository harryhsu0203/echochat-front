    init() {
        // å¾ localStorage ç²å–ä¿å­˜çš„èªè¨€
        const savedLanguage = localStorage.getItem('language');
        if (savedLanguage && this.languages[savedLanguage]) {
            this.currentLanguage = savedLanguage;
        }
        
        // æ›´æ–°èªè¨€é¸æ“‡å™¨
        this.updateLanguageSelector();
        
        // æ‡‰ç”¨ç•¶å‰èªè¨€
        this.applyLanguage(this.currentLanguage);
    },
    
    // åˆ‡æ›èªè¨€
    changeLanguage(language) {
        if (this.languages[language]) {
            this.currentLanguage = language;
            localStorage.setItem('language', language);
            this.applyLanguage(language);
            this.updateLanguageSelector();
        }
    },
    
    // æ‡‰ç”¨èªè¨€
    applyLanguage(language) {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.getTranslation(key, language);
            if (translation) {
                element.textContent = translation;
            }
        });
        
        // è™•ç† placeholder å±¬æ€§
        const placeholderElements = document.querySelectorAll('[data-i18n-placeholder]');
        placeholderElements.forEach(element => {
            const key = element.getAttribute('data-i18n-placeholder');
            const translation = this.getTranslation(key, language);
            if (translation) {
                element.placeholder = translation;
            }
        });
        
        // æ›´æ–°é é¢æ¨™é¡Œ
        const titleElement = document.querySelector('title');
        if (titleElement) {
            const titleKey = titleElement.getAttribute('data-i18n-title');
            if (titleKey) {
                const titleTranslation = this.getTranslation(titleKey, language);
                if (titleTranslation) {
                    titleElement.textContent = titleTranslation;
                }
            }
        }
        
        // æ›´æ–° html lang å±¬æ€§
        document.documentElement.lang = language;
    },
    
    // ç²å–ç¿»è­¯
    getTranslation(key, language) {
        return this.translations[language]?.[key] || this.translations['zh-TW']?.[key] || key;
    },
    
    // æ›´æ–°èªè¨€é¸æ“‡å™¨
    updateLanguageSelector() {
        const selector = document.querySelector('.language-selector');
        if (selector) {
            selector.value = this.currentLanguage;
        }
    },
    
    // ç²å–ç•¶å‰èªè¨€
    getCurrentLanguage() {
        return this.currentLanguage;
    },
    
    // ç²å–èªè¨€åç¨±
    getLanguageName(code) {
        return this.languages[code]?.name || code;
    }
